export function useLocalizedText() {
  const uiStore = useUiStore();

  const isChinese = computed(() => uiStore.language === "zh-CN");

  const text = (zh: string, en: string) => {
    if (!isChinese.value) {
      return en;
    }

    return zh || ENGLISH_TO_CHINESE[en] || en;
  };
  const bilingual = (zh: string, en: string) => `${zh || ENGLISH_TO_CHINESE[en] || en} / ${en}`;
  const roleLabel = (role?: string | null) => {
    const normalizedRole = String(role || "").trim().toLowerCase();
    const label = ROLE_LABELS[normalizedRole];

    if (!label) {
      return text("", "Member");
    }

    return isChinese.value ? label.zh : label.en;
  };

  return {
    language: computed(() => uiStore.language),
    isChinese,
    text,
    bilingual,
    roleLabel,
  };
}
